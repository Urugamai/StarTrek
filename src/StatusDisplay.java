public class StatusDisplay {
	private int statusWidth = 124;
	private int statusHeight = 700;

	public StatusDisplay(int width, int height) {
		statusWidth = width;
		statusHeight = height;
	}

	public int getStatusWidth() { return statusWidth; }
	public int getStatusHeight() { return statusHeight; }

}
